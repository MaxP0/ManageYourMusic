    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if(tfTitle.getText().isEmpty() || tfArtist.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "You missed some input lines");
        }else{
            String title = tfTitle.getText();
            String artist = tfArtist.getText();
                        
            comp = new Composition(title, artist);
            genrePlaylist2.addComp(comp);
            
            //change songs count
            lblSongsCount.setText(String.valueOf(genrePlaylist2.getCompCount()));
            
            //clean input
            tfTitle.setText("");
            tfArtist.setText("");
            
            //display playlist
            displayPlaylist();
            
        }
    }//GEN-LAST:event_btnAddActionPerformed

