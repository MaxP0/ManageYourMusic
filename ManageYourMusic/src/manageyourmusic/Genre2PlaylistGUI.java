    private void displayPlaylist() {
        playlistStr = genrePlaylist2.getPlaylistStr();
        textArea.setText("");
        for (int i = 0; i < playlistStr.size(); i++) {
            textArea.append(playlistStr.get(i) + '\n');
        }
    }
    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
        if(tfTitle.getText().isEmpty() && tfArtist.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter at least one criteria");
        }else{
            String keyword;
            
            if (!tfTitle.getText().isEmpty()){
                keyword = tfTitle.getText();
            }else{
                keyword = tfArtist.getText();
            }
            
            if(genrePlaylist2.search(keyword).isEmpty()){
                JOptionPane.showMessageDialog(null, "Nothing found");
            }else{
                //deleting composition by keyword title either artist
                genrePlaylist2.deleteComp(genrePlaylist2.search(keyword).get(0));
                
                //change songs count
                lblSongsCount.setText(String.valueOf(genrePlaylist2.getCompCount()));

                //clean input
                tfTitle.setText("");
                tfArtist.setText("");

                //display playlist
                displayPlaylist();
                
            }
        }
    }//GEN-LAST:event_btnDelActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if(tfTitle.getText().isEmpty() && tfArtist.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter at least one criteria");
        }else{
            String keyword;
            // choosing keyword
            if (!tfTitle.getText().isEmpty()){
                keyword = tfTitle.getText();
            }else{
                keyword = tfArtist.getText();
            }
            
            ArrayList<Composition> searchRes = new ArrayList<>();
            searchRes = genrePlaylist2.search(keyword);
            
            if(searchRes.isEmpty()){
                JOptionPane.showMessageDialog(null, "Nothing found");
            }else{
                textArea.setText("Search results: ");
                for (int i = 0; i < searchRes.size(); i++){
                    textArea.append("\n" + searchRes.get(i).toString());
                }

                //clean input
                tfTitle.setText("");
                tfArtist.setText("");
                
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

